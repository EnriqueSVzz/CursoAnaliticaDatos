# Ejemplo con números enteros
numero_entero = 42
print(type(numero_entero))  # <class 'int'>

# Ejemplo con números de punto flotante
numero_flotante = 3.14
print(type(numero_flotante))  # <class 'float'>

# Ejemplo con cadenas de texto
cadena = "Hola, mundo!"
print(type(cadena))  # <class 'str'>

# Ejemplo con listas
lista = [1, 2, 3, 4]
print(type(lista))  # <class 'list'>

# Ejemplo con diccionarios
diccionario = {'clave': 'valor'}
print(type(diccionario))  # <class 'dict'>