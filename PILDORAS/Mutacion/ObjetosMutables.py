# Crear una lista
numeros = [1, 2, 3]
# Modificar el segundo valor
numeros[1] = 20
print(numeros)  # [1, 20, 3]

# Crear un diccionario
persona = {'nombre': 'Ana', 'edad': 25}
# Modificar el valor de una clave
persona['edad'] = 30
print(persona)  # {'nombre': 'Ana', 'edad': 30}

# Crear un conjunto
colores = {'rojo', 'azul', 'verde'}
# AÃ±adir un elemento
colores.add('amarillo')
print(colores)  # {'rojo', 'azul', 'verde', 'amarillo'}