# Definir números complejos
numero_complejo_1 = 3 + 4j
numero_complejo_2 = 1 - 2j

# Mostrar los números complejos
print(f"Número complejo 1: {numero_complejo_1}")
print(f"Número complejo 2: {numero_complejo_2}")

# Operaciones básicas con números complejos
suma = numero_complejo_1 + numero_complejo_2
resta = numero_complejo_1 - numero_complejo_2
multiplicacion = numero_complejo_1 * numero_complejo_2
conjugado_1 = numero_complejo_1.conjugate()
conjugado_2 = numero_complejo_2.conjugate()

# Mostrar resultados
print(f"\nSuma: {suma}")
print(f"Resta: {resta}")
print(f"Multiplicación: {multiplicacion}")
print(f"Conjugado de {numero_complejo_1}: {conjugado_1}")
print(f"Conjugado de {numero_complejo_2}: {conjugado_2}")