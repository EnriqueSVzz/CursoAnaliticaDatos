# División euclidiana de dos números enteros
dividendo = 20
divisor = 3

# Calcular cociente y resto
cociente = dividendo // divisor
resto = dividendo % divisor

# Mostrar resultados
print(f"División euclidiana de {dividendo} entre {divisor}:")
print(f"Cociente: {cociente}")
print(f"Resto: {resto}")
print(f"[{dividendo}] = [{resto}] + [{cociente}]*[{divisor}]")