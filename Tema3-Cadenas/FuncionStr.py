# Crear un número entero
numero_entero = 42

# Convertir el número entero a cadena usando str()
cadena_resultante = str(numero_entero)

# Mostrar los resultados
print("Número entero:", numero_entero)
print("Tipo antes de la conversión:", type(numero_entero))

print("\nDespués de la conversión:")
print("Cadena resultante:", cadena_resultante)
print("Tipo después de la conversión:", type(cadena_resultante))