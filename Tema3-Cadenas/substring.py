# Definir una cadena de texto
cadena_original = "Python es genial"

# Obtener un substring utilizando slicing
substring_1 = cadena_original[0:6]  # Desde el índice 0 hasta el índice 5 (excluido)
substring_2 = cadena_original[7:9]  # Desde el índice 7 hasta el índice 8 (excluido)
substring_3 = cadena_original[10:]  # Desde el índice 10 hasta el final

# Mostrar los resultados
print("Cadena original:", cadena_original)
print("Substring 1:", substring_1)
print("Substring 2:", substring_2)
print("Substring 3:", substring_3)